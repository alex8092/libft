#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char	*tmp;

	tmp = (char *)s;
	if (s != NULL && n > 0)
	{
		while (n)
		{
			*tmp = 0;
			--n;
			++tmp;
		}
	}
}
